package streams;


public class HeroesPersons {

    String name;
    int age;
    boolean isMan;


    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }



    public HeroesPersons(String name, int age, boolean isMan) {
        this.name = name;
        this.age = age;
        this.isMan = isMan;
    }

    public String getName(String name){
        return name;
    }


    public void setName(String name){
        this.name = name;
    }










}
