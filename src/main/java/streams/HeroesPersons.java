package streams;


public class HeroesPersons {

    private String name;
    private int age;
    private boolean isMan;



    public HeroesPersons(String name, int age, boolean isMan) {
        this.name = name;
        this.age = age;
        this.isMan = isMan;
    }


    public int getAge() {

        return age;
    }

    public void setAge(int age) {

        this.age = age;
    }




    public String getName(String name){
        return name;
    }

    public void setName(String name){
        this.name = name;
    }










}
